// Handle search button click event
var searchButton =  inputT=document.getElementById('search');
searchButton.addEventListener('input', function() {
  var jobSearch = document.getElementById('search').value;
  filteredApplicants = filterApplicants(jobSearch);
  if (filteredApplicants.length > 0) {
    currentApplicantIndex = 0;
    displayApplicantDetails(filteredApplicants[0]);
    if (filteredApplicants.length == 1) {
      //document.getElementById("prevButton").style.display = "none";
      document.getElementById("button").style.display = "none";
    } else {
     // document.getElementById("prevButton").style.display = "none";
      document.getElementById("button").style.display = "inline";
    }
  } else {
    var applicantDetailsDiv = document.getElementById("resumeCard");
    applicantDetailsDiv.innerHTML = `<div id="err" > <img src="d.png" alt="sad">  <h1>No Such Result Found</h1>    
    </div>`;
    document.getElementById("prevButton").style.display = "none";
    document.getElementById("button").style.display = "none";
  }
});

// Handle previous button click event
var prevButton = document.getElementById("prevButton");
prevButton.addEventListener("click", function() {
 currentApplicantIndex--;
 displayApplicantDetails(filteredApplicants[currentApplicantIndex]);
 if (currentApplicantIndex == 0) {
   document.getElementById("prevButton").style.display = "none";
 }
 if (currentApplicantIndex < filteredApplicants.length - 1) {
   document.getElementById("button").style.display = "inline";
 }
});

// Handle next button click event
var nextButton = document.getElementById("button");
nextButton.addEventListener("click", function() {
  currentApplicantIndex++;
  displayApplicantDetails(filteredApplicants[currentApplicantIndex]);
 if (currentApplicantIndex > 0) {
   document.getElementById("prevButton").style.display = "inline";
 }
  if (currentApplicantIndex == filteredApplicants.length - 1) {
    document.getElementById("button").style.display = "none";}
});

