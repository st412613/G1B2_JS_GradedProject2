 // Add a listener for the page load event
 window.addEventListener('load', function() {
    // Check if the user came to the Resume page by clicking the "back" button
    if (window.performance && window.performance.navigation.type === window.performance.navigation.TYPE_BACK_FORWARD) {
      // If the user came from the login page, redirect them to the Resume page again
      window.location.replace('resume.html');
    } else {
      // If the user came to the Resume page by typing the URL or using a link, push a new history state
      window.history.pushState(null, null, window.location.href);
      window.onpopstate = function() {
        // If the user tries to go back, push a new history state and alert them
        window.history.pushState(null, null, window.location.href);
        alert('You cannot go back to the login page.');
      };
    }
  });



